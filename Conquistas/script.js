function mover(elemento){
    elemento.style.fontWeight="bolder";
    elemento.style.backgroundColor="rgba(151, 255, 220, 0.685)"
}

function sair(elemento){
    elemento.style.fontWeight="normal";
    elemento.style.backgroundColor="rgb(0,0,0,0)"
}

function entrar_but(elemento){
    elemento.style.backgroundColor=" rgb(80, 238, 225)"
}

function sair_but(elemento){    
    elemento.style.backgroundColor="rgb(195, 207, 207)"
}

function mover_link(elemento){
    elemento.style.textDecoration="underline"
}

function sair_link(elemento){
    elemento.style.textDecoration="none"
}